    private void cargarModeloTabla()
    {
        modeloTabla.addColumn("IDUsuario");
        modeloTabla.addColumn("Usuario");
        modeloTabla.addColumn("Contrase√±a");
        modeloTabla.addColumn("IDTipoUsuarioU");

        ArrayList<Usuario> listaUsuarios = base.obtenerUsuarios();
        int numeroUsuarios = listaUsuarios.size();
        modeloTabla.setNumRows(numeroUsuarios);

        for (int i = 0; i < numeroUsuarios; i++)
        {
            Usuario usuario = listaUsuarios.get(i);
            Integer id = usuario.getID();
            String nombreUsuario = usuario.getUsuario();
            String password = usuario.getPassword();
            Integer idTipo = usuario.getIDTipo();

            modeloTabla.setValueAt(id, i, 0);
            modeloTabla.setValueAt(nombreUsuario, i, 1);
            modeloTabla.setValueAt(password, i, 2);
            modeloTabla.setValueAt(idTipo, i, 3);
        }

    }

